# Generate Webots nodes for the maze
scale = 1
node_updown_template = """
v_wall {{
  translation {x} {y} 0
  scale {scale_x} {scale_y} 1
}}
"""
node_leftright_template = """
h_wall {{
    translation {x} {y} 0
    scale {scale_x} {scale_y} 1
}}
"""

nodes = []
for i in range(1, len(maze)-1):
    for j in range(1, len(maze[i])-1):
        cell = maze[i][j]
        if cell == 0:  # Wall
            up = maze[i-1][j]
            down = maze[i+1][j]
            left = maze[i][j-1]
            right = maze[i][j+1]
            if up == 1:
                x = j*scale
                y = (i - 1) * scale
                nodes.append(node_updown_template.format(x=x, y=y + 0.5, scale_x=scale, scale_y=scale/2))
            if down == 1:
                x = j*scale
                y = (i + 1) * scale
                nodes.append(node_updown_template.format(x=x, y=y - 0.5, scale_x=scale, scale_y=scale/2))
            if left == 1:
                x = (j - 1) * scale
                y = i*scale
                nodes.append(node_leftright_template.format(x=x+0.5, y=y, scale_x=scale/2, scale_y=scale))
            if right == 1:
                x = (j + 1) * scale
                y = i*scale
                nodes.append(node_leftright_template.format(x=x-0.5, y=y, scale_x=scale/2, scale_y=scale))
            

